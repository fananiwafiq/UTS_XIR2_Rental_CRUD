    public void selectData(){
       String kolom[] = {"Nama","Alamat Peminjam","NoStruk","NoPolisi","TanggalPinjam","TanggalKembali","Harga"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM db_rental";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next()){
               String Nama = rs.getString(1);
               String Alamat = rs.getString(2);
               String STRUK = rs.getString(3);
               String Pol = rs.getString(4);
               String Pinjam = rs.getString(5);
               String Kembali = rs.getString(6);
               String Harga = rs.getString(7);
               String data [] = {Nama,Alamat,STRUK,Pol,Pinjam,Kembali,Harga};
               dtm.addRow(data);
           }   
        }catch(SQLException ex){
            Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
        }
       tbRental.setModel(dtm);
    
    }
